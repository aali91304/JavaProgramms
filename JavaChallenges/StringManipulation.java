package JavaChallenges;

public class StringManipulation {
    public static void main(String[] args) {
        String FirstName = "Akhtar";
        String Lastname = "Ali";
        String Concat = FirstName.concat( " ").concat(Lastname);
        //Concatination
        System.out.print("After Merging Two String : " + Concat);
        //Convert int Uppercase
        System.out.println();
        System.out.print(Concat.toUpperCase());



    }

}
